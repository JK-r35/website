    <h2 style="color: aliceblue;">Deep-sea anglerfish</h2>
    <p style="font-size : large; color: aliceblue;">The anglerfish are ray-finned fish in the order Lophiiformes. Both the order's common and scientific name comes from the characteristic mode of predation, in which a modified dorsal fin ray acts as a lure for prey (akin to a human angler, and likened to a crest or "lophos"). The modified fin ray, with the very tip being the esca and the length of the structure the illicium, is adapted to attract specific prey items across the families of anglerfish by using different luring methods.</p><br>
    <p style="font-size: large; color:aliceblue;">Anglerfish are notable for their sexual dimorphism, which is sometimes extremely pronounced; the males may be several orders of magnitude smaller in mass than females. This dimorphism has enabled a unique reproductive method in the deep-sea anglerfish; sexual parasitism is the attachment of male to the much larger female, sometimes fusing together as an example of natural parabiosis.</p>
