          <h2 style="color: aliceblue;"> deepsea  dragonfish (Stomidae)</h2>
          <p style="font-size: large; color:white;">Measuring just 15 centimeters (6 inches) in length, the dragonfish (Aristostomias scintillans) has an “enormous” jaw relative to their size, capable of extending and opening beyond the abilities of a conventional jaw. It’s also lined with dozens of fang-like teeth sharper than those found in a piranha. To keep their prey in the dark, the teeth of dragonfish have evolved a transparent structure that essentially makes their fearsome mouth invisible.</p><br>
          <p style="font-size: large; color: azure;">Dragonfish have small photophores that generate light for attracting prey, but with such big teeth, they’d risk giving the game away if their spiky gnashers reflected that light. Research has shown that they’ve evolved transparent teeth to overcome this, meaning they’re both excellent predators and master hiders.
          </p>
