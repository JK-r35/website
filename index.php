          <h2 style="color: aliceblue;"> Barreleye (Macropinna)</h2>
          <p style="font-size: large; color: aliceblue;"> Found at depths most light can't reach, these truly bizarre animals are equipped with a see-through head, allowing their eyes to effectively look out of the sunroof as they navigate the pitch-black waters.The barreleye’s eyes aren’t those two indentations you see in the conventional eye position, but are actually the glowy green balls sitting slightly further back. The circles at the front are olfactory organs that can “smell” chemical cues in the water. You can see a living barreleye swimming here.</p><br>
           <p style="font-size: large; color: aliceblue;"> The function of the eyes’ strange positioning, MBARI says, is to spy prey above them, which they’ll sometimes steal from the dangling tentacles of siphonophores. As ultra-sensitive tubular eyes, they are incredibly well adapted for spotting the silhouettes of edible animals against the minuscule amount of light that travels to such depths. At one point, it was thought the barreleye’s eyes were constantly to the sky, but it’s since been established that they can roll to face forwards when eating.</p>
        
        
